// Canvas
const canvas = document.querySelector('canvas.webgl')

// Scene
const scene = new THREE.Scene()

/**
 * Models
 */
const dracoLoader = new DRACOLoader()
dracoLoader.setDecoderPath('/draco/')

const gltfLoader = new GLTFLoader()
gltfLoader.setDRACOLoader(dracoLoader)

let mixer = null

gltfLoader.load(
    '/models/racetrack_final.glb',
    (gltf) =>
    {
        gltf.scene.scale.set(.5, .5, .5 )
        gltf.scene.position.set(-.8,0,0)
        scene.add(gltf.scene)

        // gui.add(gltf.scene.position, 'x').min(-5).max(5).step(0.001).name('x')
        // gui.add(gltf.scene.position, 'y').min(-5).max(5).step(0.001).name('y')
        // gui.add(gltf.scene.position, 'z').min(-5).max(5).step(0.001).name('z')

        // Animation
        mixer = new THREE.AnimationMixer(gltf.scene)
        

        for (let i = 0; i < gltf.animations.length; i++){
            const action = mixer.clipAction(gltf.animations[i])
            action.play()
        }
    }
)

